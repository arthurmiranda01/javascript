//O que é síncrono e assíncrono?

//Síncrono: Na ordem de execução, uma ação espera pela outra. Exemplo:

let nome = arthur; //Executa esse primeiro
let sobrenome = miranda; //Depois esse
let nomeCompleto = nome + sobrenome; //Pra finalizar com esse, ou seja, segue uma ordem.

//Assíncrono: Vai executar tudo de uma vez só, uma ação não irá aguardar a outra.